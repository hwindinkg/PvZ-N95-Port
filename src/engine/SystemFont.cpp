SystemFont::SystemFont()
    : Font()
{
    mFontHeight = 8;
    mAscent = 8;
    mMaxCharWidth = 8;
}

SystemFont::~SystemFont()
{
}

bool SystemFont::Initialize()
{
    if (sFontSheet)
        return true;

    // 16 cols x 7 rows of 8x8 glyphs = 128x56 image. But we only have 96
    // glyphs (32-127). Use 16 cols x 6 rows = 96 glyphs, image 128x48.
    const int kCols = 16;
    const int kRows = 6;
    const int kCellW = 8;
    const int kCellH = 8;
    const int imgW = kCols * kCellW;   // 128
    const int imgH = kRows * kCellH;   // 48

    sFontSheet = new MemoryImage();
    if (!sFontSheet)
        return false;
    sFontSheet->SetSize(imgW, imgH);

    // Fill with transparent black.
    unsigned int* pixels = (unsigned int*)sFontSheet->GetBits();
    if (!pixels)
    {
        delete sFontSheet;
        sFontSheet = NULL;
        return false;
    }
    for (int i = 0; i < imgW * imgH; i++)
        pixels[i] = 0x00000000;

    // Render each glyph (ASCII 32-127) into the sheet as white opaque pixels.
    for (int g = 0; g < 96; g++)
    {
        int col = g % kCols;
        int row = g / kCols;
        int baseX = col * kCellW;
        int baseY = row * kCellH;
        const unsigned char* glyph = kGlyphs8x8[g];
        for (int y = 0; y < 8; y++)
        {
            unsigned char rowBits = glyph[y];
            for (int x = 0; x < 8; x++)
            {
                if (rowBits & (0x80 >> x))
                {
                    int px = baseX + x;
                    int py = baseY + y;
                    pixels[py * imgW + px] = 0xFFFFFFFF;  // white opaque
                }
            }
        }
    }

    if (!sInstance)
        sInstance = new SystemFont();
    return true;
}

SystemFont* SystemFont::Get()
{
    if (!sFontSheet && !Initialize())
        return NULL;
    return sInstance;
}

void SystemFont::DrawString(Graphics* g, int x, int y,
                            const char* text, int /*length*/,
                            const Color* theColor)
{
    if (!g || !text || !sFontSheet)
        return;

    if (theColor)
        g->SetColor(*theColor);

    // [M4 #4 diag] Log first DrawString call to verify it's reached.
    static bool sLoggedOnce = false;
    if (!sLoggedOnce)
    {
        sLoggedOnce = true;
        // Write to gfx_log.txt (append).
        RFs fs; RFile f;
        if (fs.Connect() == KErrNone)
        {
            fs.MkDirAll(_L("C:\\Data\\PvZ"));
            if (f.Open(fs, _L("C:\\Data\\PvZ\\gfx_log.txt"),
                       EFileWrite | EFileShareAny) == KErrNone)
            {
                TInt pos = 0; f.Seek(ESeekEnd, pos);
                TBuf8<128> msg;
                msg.Format(_L8("SystemFont::DrawString first call: '%s' at %d,%d sheet=%08x bits=%08x\n"),
                           (const TUint8*)text, x, y, (TUint)sFontSheet,
                           (TUint)sFontSheet->GetBits());
                f.Write(msg);
                f.Flush();
                f.Close();
            }
            fs.Close();
        }
    }

    int curX = x;
    while (*text)
    {
        unsigned char c = (unsigned char)*text;
        if (c >= 32 && c < 128)
        {
            int gIdx = c - 32;
            const unsigned char* glyph = kGlyphs8x8[gIdx];
            // Render glyph as filled rects (1px each) -- avoids texture
            // upload issues with the font sheet. Slower but guaranteed visible.
            // Use the current color (set by caller via SetColor).
            for (int gy = 0; gy < 8; gy++)
            {
                unsigned char rowBits = glyph[gy];
                for (int gx = 0; gx < 8; gx++)
                {
                    if (rowBits & (0x80 >> gx))
                    {
                        g->FillRect(curX + gx, y - 8 + gy, 1, 1);
                    }
                }
            }
        }
        curX += 8;
        text++;
    }
}

int SystemFont::StringWidth(const char* text)
{
    if (!text)
        return 0;
    int len = 0;
    while (text[len] != '\0') len++;
    return len * 8;
}

